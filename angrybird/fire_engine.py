"""
angrybird/fire_engine.py

ELMFIRE wrapper for the IGNIS pipeline.

Public surface:
    ElmfireEngine   — core wrapper; takes CycleSnapshot + EnsembleConfig
    ElmfireAdapter  — satisfies FireEngineProtocol; called by orchestrator/runner

The orchestrator calls:
    fire_engine.run(terrain, gp_prior, fire_state, n_members, horizon_min, rng)

ElmfireAdapter.run() translates that into:
    1. Draw GP-scaled perturbation fields from gp_prior + rng
    2. Build CycleSnapshot and EnsembleConfig
    3. Delegate to ElmfireEngine.run()

ElmfireEngine never changes its own interface — all adaptation lives here.

Dependencies: rasterio, numpy (both already in requirements.txt)
"""

